#include "mpi.h"

/*=============================================================================================*/
/* Same behavior as PMPI_Issend */
int PMPI_Irsend (void* message, int count, MPI_Datatype datatype, int dest,
        int tag, MPI_Comm comm, MPI_Request* request)
{
  return PMPI_Issend(message, count, datatype, dest, tag, comm, request);
}
